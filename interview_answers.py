def analyze_csv(filepath):
    """
    Loads a CSV, prints the 'First 5 Minutes' checklist,
    and returns a summary dictionary.
    """
    df = pd.read_csv(filepath)

    print("\n=== First 5 Minutes Checklist ===")
    print(f"Shape        : {df.shape}")
    print(f"Columns      : {list(df.columns)}")
    print(f"Data Types   :\n{df.dtypes}")
    print(f"Missing vals :\n{df.isnull().sum()}")
    print(f"Head:\n{df.head()}")
    print(f"Describe:\n{df.describe()}")

    numeric_cols = list(df.select_dtypes(include=[np.number]).columns)
    categorical_cols = list(df.select_dtypes(include=["object", "category"]).columns)
    null_counts = df.isnull().sum().to_dict()
    memory_mb = round(df.memory_usage(deep=True).sum() / (1024 ** 2), 4)

    result = {
        "num_rows": df.shape[0],
        "num_cols": df.shape[1],
        "numeric_cols": numeric_cols,
        "categorical_cols": categorical_cols,
        "null_counts": null_counts,
        "memory_mb": memory_mb
    }

    print("\n=== Summary Dict ===")
    for key, val in result.items():
        print(f"  {key}: {val}")

    return result


