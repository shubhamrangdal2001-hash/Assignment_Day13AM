print("\n--- Q3: Debugging 3 Bugs ---")

df = pd.DataFrame({
    "name": ["Alice", "Bob", "Charlie"],
    "age":  [25, 30, 35],
    "salary": [50000, 60000, 70000]
})

# Bug 1 Fix: use & (bitwise) instead of 'and' (Python keyword)
# Original (buggy): df[df["age"] > 25 and df["salary"] > 55000]
# Fix: use & with parentheses around each condition
high_earners = df[(df["age"] > 25) & (df["salary"] > 55000)]
print(f"\nBug 1 Fixed - high_earners:\n{high_earners}")
print("  Explanation: Use '&' for element-wise AND on Series, not 'and'.")
print("  Also wrap each condition in parentheses due to operator precedence.")

# Bug 2 Fix: use .loc[] for assignment instead of chained indexing
# Original (buggy): df["age"][0] = 26  -> raises SettingWithCopyWarning
# Fix: use df.loc[index, column]
df.loc[0, "age"] = 26
print(f"\nBug 2 Fixed - df.loc[0, 'age'] updated to: {df.loc[0, 'age']}")
print("  Explanation: Chained indexing df['col'][idx] may modify a copy.")
print("  Use df.loc[row_label, col_label] for safe in-place assignment.")

# Bug 3 Fix: iloc[0:3] actually returns 3 rows (positions 0,1,2) - that IS correct
# The bug is the comment expecting 3 rows from [0:2]
# iloc[0:2] gives rows at index 0 and 1 only (2 rows), not 3
first_three = df.iloc[0:3]   # corrected slice to get 3 rows
print(f"\nBug 3 Fixed - first_three rows: {len(first_three)}")
print(first_three)
print("  Explanation: df.iloc[0:2] gives only 2 rows (positions 0 and 1).")
print("  iloc slicing is exclusive on the end, so use iloc[0:3] for 3 rows.")

