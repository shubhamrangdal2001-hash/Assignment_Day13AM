# Q1: .loc[] vs .iloc[] difference
# -----------------------------------------------
print("\n--- Q1: .loc[] vs .iloc[] ---")

# When index is 0,1,2,3,4
df_numeric = pd.DataFrame({
    "name": ["Alice", "Bob", "Charlie", "Diana", "Eve"],
    "score": [85, 90, 78, 92, 88]
})

print("\nDataFrame with numeric index (0,1,2,3,4):")
print(df_numeric)

print("\ndf.loc[0:3]  -- label-based, INCLUSIVE of 3:")
print(df_numeric.loc[0:3])

print("\ndf.iloc[0:3] -- position-based, EXCLUSIVE of 3:")
print(df_numeric.iloc[0:3])

# When index is 'a','b','c','d','e'
df_str = df_numeric.copy()
df_str.index = ['a', 'b', 'c', 'd', 'e']

print("\nDataFrame with string index ('a','b','c','d','e'):")
print(df_str)

print("\ndf.loc['a':'c']  -- label-based, includes 'c':")
print(df_str.loc['a':'c'])

print("\ndf.iloc[0:3]     -- still position-based, excludes position 3:")
print(df_str.iloc[0:3])

print("""
Key difference summary:
  .loc[]  → uses LABELS (row names/index values), end is INCLUSIVE
  .iloc[] → uses INTEGER POSITIONS (0-based), end is EXCLUSIVE

When index is 0,1,2,3,4:
  df.loc[0:3]  → returns rows 0,1,2,3  (4 rows - inclusive)
  df.iloc[0:3] → returns rows 0,1,2    (3 rows - exclusive)

When index is 'a','b','c','d','e':
  df.loc['a':'c']  → returns rows a,b,c (label-inclusive)
  df.iloc[0:3]     → still returns rows at positions 0,1,2 = a,b,c
""")

